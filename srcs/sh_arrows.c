#include "minishell.h"

void	sh_left(int *pos)
{
	if (*pos > 0)
	{
		write(1, "\33[D", 4);
		--(*pos);
	}
}

void	sh_right(int *pos, int len)
{
	if (*pos < len)
	{
		write(1, "\33[C", 4);
		++(*pos);
	}
}
