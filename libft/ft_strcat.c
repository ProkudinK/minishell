#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	len;
	size_t	i;

	i = 0;
	len = 0;
	len = ft_strlen(s1);
	while (*(s2 + i))
	{
		*(s1 + len) = *(s2 + i);
		i++;
		len++;
	}
	*(s1 + len) = '\0';
	return (s1);
}
